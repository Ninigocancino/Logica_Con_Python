# Crea una lista llamada numeros que guarde los elementos 10,20,30,40,50. Luego inicia un ciclo donde el iterador numero recorra los elementos de la lista numeros y tome el valor actual en cada iteracion. Después verifica si el iterador numero es mayor a 30 si la condición se cumple imprime consola el valor del iterador numero en cada iteración

numeros = [10,20.30,40,50]
for numero in numeros:
    if numero > 30:
        print(numero)