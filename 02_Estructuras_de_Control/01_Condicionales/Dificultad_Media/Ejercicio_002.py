
#Instrucciones en lenguajes natural: Declara una variable llamada número a la que le asignes un valor numérico 7, luego verifica si la variable numero es divisible entre dos y su residuo es igual 0. Si la condición se cumple, entonces imprime en consola el mensaje "El número es par". Pero si la condición es  falsa entonces imprime el mensaje "El número es impar"


numero = 7
if numero % 2 == 0:
    print("El número es par")
else:
    print("El número es impar")