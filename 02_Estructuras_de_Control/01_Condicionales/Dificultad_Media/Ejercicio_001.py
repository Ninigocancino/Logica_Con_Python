
#Instrucciones en lenguaje natural: Declara una variable llamada 'edad', guarda en dicha variable el valor 20. Luego verifica si la variable 'edad' es mayor o igual que 18. Si la condición es verdadera, entonces imprime en consola el mensaje 'Eres mayor de edad'. Pero si la condición es falsa, entonces imprime en consola el mensaje 'Eres menor de edad.

#Instrucciones en Python:
edad = 20
if edad >= 18:
    print('Eres mayor de edad')
else:
    print('Eres menor de edad')